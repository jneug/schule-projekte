
public class Word {

    //ml*
    private String english;
    private String german;

    public Word( String pEnglish, String pGerman ) {
        english = pEnglish;
        german = pGerman;
    }

    public String getGerman() {
        return german;
    }

    public String getEnglish() {
        return english;
    }
    //*ml

}
